        with open(param_card, 'r') as file:
            data = file.readlines()

        for i in range(0, len(data)):
            if "APMASS" in data[i]:
                data[i] = "       622     %.7fe-03   # APMASS" % (self.apmass) + '\n'
                logger.info("APMASS in param card set to %d" % self.apmass)
